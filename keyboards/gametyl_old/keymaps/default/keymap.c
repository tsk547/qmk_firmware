// Defines names for use in layer keycodes and the keymap
enum layer_names {
    _QWERTY,
    _EX,
    _FFXIV,
    _FFCHAT,
    _FFEX
};

static uint16_t default_animation = RGB_MATRIX_CYCLE_SPIRAL;
static int default_speed = 50;

// Init
void keyboard_post_init_user(void) {
    rgb_matrix_sethsv_noeeprom(HSV_PURPLE);
    rgb_matrix_mode_noeeprom(default_animation);
    rgb_matrix_set_speed_noeeprom(default_speed);
}

enum custom_keycodes {
    // VVV = KC_TRNS,
    // XXX = KC_NO,
    EnSH = MT(MOD_LSFT, KC_ENT),
    OSCHAT = OSL(_FFCHAT)
};

enum macro_events {
    M_CHAT = SAFE_RANGE,
    M_ECHAT,
    M_JUMP
};

bool spam_space;
uint16_t spam_timer = false;
uint16_t spam_interval = 100;

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) { // This will do most of the grunt work with the keycodes.
    case M_CHAT:
      if (record->event.pressed) {
        tap_code(KC_ENT);
        layer_move(_FFCHAT);
      } 
      break;
      return false;
    case M_ECHAT:
      if (record->event.pressed) {
        tap_code(KC_ESC);
        layer_move(_FFXIV);
      } 
      break;
      return false;
    case M_JUMP:
      if (record->event.pressed){
        spam_space ^= 1; 
        spam_timer = timer_read();
      } else {

      }
      break;
      return false;
  }
  return true;
}

void matrix_scan_user(void) {
  if (spam_space && timer_elapsed(spam_timer) >= spam_interval) {
     spam_timer = timer_read();
     tap_code(KC_SPC);
  }
}

